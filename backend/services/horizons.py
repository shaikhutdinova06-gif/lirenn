# Научные горизонты почв (из учебника почвоведения)
# AU — гумусовый горизонт
# AY — дерновый горизонт
# E — подзолистый горизонт (элювиальный)
# BT — иллювиальный горизонт (накопления)
# BCA — карбонатный горизонт
# G — глеевый горизонт
# C — материнская порода

HORIZONS = {
    "chernozem": ["AU", "BCA", "C"],
    "podzolic": ["AY", "E", "BT", "C"],
    "gray_forest": ["AU", "E", "BT", "C"],
    "gley": ["G", "C"],
    "chestnut": ["AU", "BCA", "C"],
    "tundra_gley": ["G", "C"]
}

HORIZON_DESCRIPTIONS = {
    "AU": "Гумусовый горизонт - тёмный, богатый органикой",
    "AY": "Дерновый горизонт - корни трав, светло-серый",
    "E": "Подзолистый горизонт - вымывание, белёсый",
    "BT": "Иллювиальный горизонт - накопление, тёмно-коричневый",
    "BCA": "Карбонатный горизонт - накопление кальция",
    "G": "Глеевый горизонт - переувлажнение, сизый",
    "C": "Материнская порода - неизменённый субстрат"
}

HORIZON_COLORS = {
    "AU": "#2e1c0b",      # Тёмно-коричневый (гумус)
    "AY": "#5a4a3f",      # Светло-коричневый (дерновый)
    "E": "#f5f5f5",       # Белёсый (подзол)
    "BT": "#7b4f2c",      # Коричневый (иллювиальный)
    "BCA": "#8b7355",     # Светло-коричневый (карбонатный)
    "G": "#6d7f8f",       # Сизый (глеевый)
    "C": "#999999"        # Серый (порода)
}

def get_horizons(soil_type):
    """Возвращает горизонты для типа почвы"""
    return HORIZONS.get(soil_type, ["AU", "C"])

def get_horizon_description(horizon):
    """Возвращает описание горизонта"""
    return HORIZON_DESCRIPTIONS.get(horizon, "Неизвестный горизонт")

def get_horizon_color(horizon):
    """Возвращает цвет горизонта"""
    return HORIZON_COLORS.get(horizon, "#8B4513")
