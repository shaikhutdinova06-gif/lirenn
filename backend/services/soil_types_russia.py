def get_all_soil_types():
    """Получить список всех типов почв"""
    return list(SOIL_TYPES_RUSSIA.keys())

def get_soil_name(code):
    """Получить название почвы по коду"""
    return SOIL_TYPES_RUSSIA.get(code, code)

def get_soil_list():
    """Получить список почв для frontend"""
    return list(SOIL_TYPES_RUSSIA.values())

def search_soil_by_name(name):
    """Поиск почвы по названию (возвращает код)"""
    name = name.lower()
    for code, soil_name in SOIL_TYPES_RUSSIA.items():
        if name in soil_name.lower() or soil_name.lower() in name:
            return code
    return None

def get_soil_by_partial_match(partial_name):
    """Поиск почвы по частичному совпадению названия"""
    partial_name = partial_name.lower()
    matches = []
    for code, soil_name in SOIL_TYPES_RUSSIA.items():
        if partial_name in soil_name.lower():
            matches.append({"code": code, "name": soil_name})
    return matches
